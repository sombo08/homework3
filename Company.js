function findValueByKey(companyName) {
    const client = company.clients.find(client => client.name === companyName);
    if (client) {
        return {
            name: client.name,
            type: client.type,
            uses: client.uses,
            sells: client.sells,
            partners: client.partners || []
        };
    } else {
        return null;
    }
}

